def get_num_fish(timers: Iterable[int], num_days: int) -> int:
    current_fish = deque(timers)

    for _ in range(num_days):
        new_fish = deque()
        while current_fish:
            timer = current_fish.popleft()
            if timer == 0:
                new_fish.append(6)
                new_fish.append(8)
            else:
                new_fish.append(timer - 1)
        current_fish = new_fish
    return len(current_fish)


